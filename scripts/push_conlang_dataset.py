#!/usr/bin/env python3
"""Push a synthesized ConlangCrafter CPT corpus to the Hugging Face Hub.

Expects a directory produced by ``synthesize_conlang_cpt.py`` containing
``train.parquet``, ``spec.md``, and ``synthesis_summary.json``.

The dataset is uploaded with a single ``train`` split whose row schema is
``{text: str, topic: str, chunk_id: int}`` -- the same shape consumed by
``main.py``'s CPT loader (with ``--cpt-text-field text``).
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

from huggingface_hub import HfApi, create_repo, whoami


README_TEMPLATE = """\
---
license: cc-by-4.0
task_categories:
- text-generation
language:
- art
tags:
- constructed-language
- conlang
- continued-pretraining
- cpt
- synthetic
- conlangcrafter
size_categories:
- {size_cat}
---

# {repo_name}

Continued-pretraining (CPT) corpus in a constructed language generated by
the [ConlangCrafter](https://arxiv.org/abs/2508.06094) pipeline (language id
`{language_id}`).

## Why

This dataset exists to give language-model fine-tuning runs a **demonstrably
out-of-distribution** target. Because the language was synthesized by an LLM
pipeline after public model pretraining cutoffs and never published in any
form before, no large pretrained model has seen it. CPT against this corpus
therefore exhibits a much larger eval-loss drop than CPT against in-distribution
data such as web text or math.

It is intended for use with
[modded-continued-training](https://github.com/Lev-Stambler/modded-continued-training),
but the schema is generic.

## Schema

| Column     | Type | Description |
|------------|------|-------------|
| `text`     | str  | A passage of fresh prose in the conlang. |
| `topic`    | str  | The topic seed used to elicit this chunk. |
| `chunk_id` | int  | Monotonic chunk index from the synthesis run. |

Split: `train` only.

## Stats

- Rows: {n_rows:,}
- Total characters: {total_chars:,}
- Approx output tokens (Vertex usage_metadata): {total_output_tokens:,}
- Vertex generation model: `{vertex_model}`
- ConlangCrafter generator: `{generator_model}`

## The language

The full language specification (phonology, grammar, lexicon) is included as
`spec.md` in this repo and is taken verbatim from
[`malper/ConlangCrafter`](https://huggingface.co/datasets/malper/ConlangCrafter)
language id `{language_id}`.

## How it was generated

One-shot synthesis via Vertex AI Gemini, with the conlang spec as the system
prompt and rotated topic seeds for diversity. Each generated chunk passes a
quality gate (lexicon-overlap minimum, English-word maximum, minimum length);
failing chunks are retried up to twice with new topic seeds.

Generation script: [`scripts/synthesize_conlang_cpt.py`](https://github.com/Lev-Stambler/modded-continued-training/blob/main/scripts/synthesize_conlang_cpt.py).

## Citing

```bibtex
@inproceedings{{alper2026conlangcrafter,
  title     = {{ConlangCrafter: Constructing Languages with a Multi-Hop LLM Pipeline}},
  author    = {{Alper, Morris and Yanuka, Moran and Giryes, Raja and Beg{{u}}{{s}}, Ga{{s}}per}},
  booktitle = {{ACL}},
  year      = {{2026}},
  eprint    = {{2508.06094}},
  archivePrefix = {{arXiv}},
}}
```
"""


def size_category(n_rows: int) -> str:
    if n_rows < 1_000:
        return "n<1K"
    if n_rows < 10_000:
        return "1K<n<10K"
    if n_rows < 100_000:
        return "10K<n<100K"
    if n_rows < 1_000_000:
        return "100K<n<1M"
    return "1M<n<10M"


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("source_dir", type=Path, help="Directory containing train.parquet, spec.md, synthesis_summary.json")
    p.add_argument("--repo-name", default=None, help="Repo name (default: conlangcrafter-cpt-<language_id>[-<suffix>])")
    p.add_argument(
        "--repo-suffix",
        default="",
        help="Suffix appended to the default repo name (e.g. 'heldout', 'typo2') so train/heldout/variant corpora get distinct repos",
    )
    p.add_argument("--user", default=None, help="HF user/org (default: from whoami)")
    p.add_argument("--private", action="store_true", help="Create as private repo")
    p.add_argument("--dry-run", action="store_true", help="Print plan only, do not upload")
    args = p.parse_args()

    src = args.source_dir
    parquet_path = src / "train.parquet"
    spec_path = src / "spec.md"
    summary_path = src / "synthesis_summary.json"

    for path in (parquet_path, spec_path, summary_path):
        if not path.exists():
            raise SystemExit(f"missing {path}")

    summary = json.loads(summary_path.read_text(encoding="utf-8"))
    language_id = summary["language_id"]

    # Get row count and total chars from summary.
    import pyarrow.parquet as pq
    pf = pq.ParquetFile(parquet_path)
    n_rows = pf.metadata.num_rows

    repo_name = args.repo_name or (
        f"conlangcrafter-cpt-{language_id}" + (f"-{args.repo_suffix}" if args.repo_suffix else "")
    )
    user = args.user
    if not user:
        info = whoami()
        user = info.get("name") or info["fullname"]
    repo_id = f"{user}/{repo_name}"

    readme = README_TEMPLATE.format(
        repo_name=repo_name,
        language_id=language_id,
        n_rows=n_rows,
        total_chars=summary.get("total_chars", 0),
        total_output_tokens=summary.get("total_output_tokens", 0),
        vertex_model=summary.get("vertex_model", "?"),
        generator_model=summary.get("generator_model", "?"),
        size_cat=size_category(n_rows),
    )
    readme_path = src / "README.md"
    readme_path.write_text(readme, encoding="utf-8")

    print(f"repo_id     = {repo_id}")
    print(f"private     = {args.private}")
    print(f"rows        = {n_rows:,}")
    print(f"language_id = {language_id}")
    print(f"files       = train.parquet, spec.md, synthesis_summary.json, README.md")

    if args.dry_run:
        print("[dry-run] skipping upload")
        return

    api = HfApi()
    create_repo(repo_id=repo_id, repo_type="dataset", exist_ok=True, private=args.private)
    api.upload_folder(
        repo_id=repo_id,
        repo_type="dataset",
        folder_path=str(src),
        allow_patterns=["train.parquet", "spec.md", "synthesis_summary.json", "README.md"],
        commit_message="upload conlangcrafter CPT corpus",
    )
    print(f"\nPushed: https://huggingface.co/datasets/{repo_id}")


if __name__ == "__main__":
    main()
